char	*ft_strcapitalize(char *str)
{
	int i;

	i = 1;
	if (str[0] > 96 && str[0] < 123)
		str[0] = str[0] - 32;
	while (str[i] != '\0')
	{
		if (('a' <= str[i] && str[i] <= 'z') && (('Z' < str[i - 1]
			&& str[i - 1] < 'a') || (str[i - 1] < 'A' && str[i - 1] > '9')
			|| (str[i - 1] < '0' && str[i - 1] >= '!') || str[i - 1] > 'z'
			|| str[i - 1] == ' '))
			str[i] = str[i] - 32;
		if (('A' <= str[i] && str[i] <= 'Z') && ((str[i - 1] <= 'z'
							&& str[i - 1] >= 'a') || (str[i - 1] <= '9'
								&& str[i - 1] >= '0') || (str[i - 1] >= 'A'
									&& str[i - 1] <= 'Z')))
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}

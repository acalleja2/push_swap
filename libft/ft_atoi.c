#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int negative;
	int sum;

	i = 0;
	negative = 0;
	sum = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\r' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		i++;
		negative = 1;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum = sum * 10 + (str[i] - 48);
		i++;
	}
	if (negative == 1)
		return (-sum);
	return (sum);
}
